import { consoleLog } from '../modules/utils';

// +-------+-------------------------------------------------------------------+
// | Title | Dependency                                                        |
// +-------+-------------------------------------------------------------------+

// Environment
// -----------------------------------------------------------------------------

const env = process.env.NODE_ENV;

// +-------+-------------------------------------------------------------------+
// | Title | Configs                                                           |
// +-------+-------------------------------------------------------------------+

// Part config
// -----------------------------------------------------------------------------

/**
 * Webpack mode part
 *
 * @param {any} mode - Mode value
 */

const config = ({ mode }: any): any => (
  consoleLog.modeLog(),

  consoleLog.propsLog({
    prop: mode,
    msg: 'The "mode" property in "mode.ts" was changed!',
  }),

  {
    mode: mode || env || 'development',
  }
);

// +-------+-------------------------------------------------------------------+
// | Title | Exports                                                           |
// +-------+-------------------------------------------------------------------+

// Part config
// -----------------------------------------------------------------------------

export default config;
